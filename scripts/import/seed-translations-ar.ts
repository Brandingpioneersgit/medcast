type EntityRow = { id: number; slug: string };

async function seedGroup(
  sql: ReturnType<typeof postgres>,
  translatableType: "country" | "specialty" | "treatment" | "condition",
  dict: Dict,
  rows: EntityRow[],
): Promise<{ inserted: number; missing: string[] }> {
  const translations: Array<{
    translatable_type: string;
    translatable_id: number;
    locale: string;
    field_name: string;
    value: string;
    is_machine_translated: boolean;
    is_reviewed: boolean;
  }> = [];
  const seen = new Set<string>();

  for (const row of rows) {
    const entry = dict[row.slug];
    if (!entry) continue;
    seen.add(row.slug);
    translations.push({
      translatable_type: translatableType,
      translatable_id: row.id,
      locale: LOCALE,
      field_name: "name",
      value: entry.name,
      is_machine_translated: false,
      is_reviewed: true,
    });
    if (entry.description) {
      translations.push({
        translatable_type: translatableType,
        translatable_id: row.id,
        locale: LOCALE,
        field_name: "description",
        value: entry.description,
        is_machine_translated: false,
        is_reviewed: true,
      });
    }
  }

  const missing = Object.keys(dict).filter((slug) => !seen.has(slug));

  if (DRY || translations.length === 0) return { inserted: translations.length, missing };

  await sql`
    INSERT INTO translations ${sql(translations)}
    ON CONFLICT (translatable_type, translatable_id, locale, field_name)
    DO UPDATE SET
      value = EXCLUDED.value,
      is_machine_translated = EXCLUDED.is_machine_translated,
      is_reviewed = EXCLUDED.is_reviewed,
      updated_at = NOW()
  `;

  return { inserted: translations.length, missing };
}

async function main() {
  const connectionString = process.env.DATABASE_URL;
  if (!connectionString) throw new Error("DATABASE_URL is required");
  const sql = postgres(connectionString);

  const [countryRows, specialtyRows, treatmentRows, conditionRows] = await Promise.all([
    sql`SELECT id, slug FROM countries WHERE is_destination = true` as Promise<EntityRow[]>,
    sql`SELECT id, slug FROM specialties WHERE is_active = true` as Promise<EntityRow[]>,
    sql`SELECT id, slug FROM treatments WHERE is_active = true` as Promise<EntityRow[]>,
    sql`SELECT id, slug FROM conditions` as Promise<EntityRow[]>,
  ]);

  const countriesRes = await seedGroup(sql, "country", COUNTRIES, countryRows);
  const specialtiesRes = await seedGroup(sql, "specialty", SPECIALTIES, specialtyRows);
  const treatmentsRes = await seedGroup(sql, "treatment", TREATMENTS, treatmentRows);
  const conditionsRes = await seedGroup(sql, "condition", CONDITIONS, conditionRows);

  const prefix = DRY ? "would insert" : "inserted";
  console.log(`${prefix} ${countriesRes.inserted} country rows (dict has ${Object.keys(COUNTRIES).length})`);
  console.log(`${prefix} ${specialtiesRes.inserted} specialty rows (dict has ${Object.keys(SPECIALTIES).length})`);
  console.log(`${prefix} ${treatmentsRes.inserted} treatment rows (dict has ${Object.keys(TREATMENTS).length})`);
  console.log(`${prefix} ${conditionsRes.inserted} condition rows (dict has ${Object.keys(CONDITIONS).length})`);

  const allMissing = [
    ...countriesRes.missing.map((s) => `country:${s}`),
    ...specialtiesRes.missing.map((s) => `specialty:${s}`),
    ...treatmentsRes.missing.map((s) => `treatment:${s}`),
    ...conditionsRes.missing.map((s) => `condition:${s}`),
  ];
  if (allMissing.length > 0) {
    console.log(`\nDict entries with no matching slug in DB (${allMissing.length}):`);
    allMissing.slice(0, 30).forEach((m) => console.log(`  - ${m}`));
    if (allMissing.length > 30) console.log(`  ...and ${allMissing.length - 30} more`);
  }

  await sql.end();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
